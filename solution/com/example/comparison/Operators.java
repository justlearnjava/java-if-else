package com.example.comparison;

public class Operators {

  public boolean compareAEqualsB(final int a, final int b) {
    // Compare a and b using ==
    return a == b;
  }

  public boolean compareANotEqualsB(final int a, final int b) {
    // Compare a and b using !=
    return a != b;
  }

  public boolean compareAGreaterThanB(final int a, final int b) {
    // Compare a and b using >
    return a > b;
  }

  public boolean compareAGreaterThanOrEqualToB(final int a, final int b) {
    // Compare a and b using >=
    return a >= b;
  }

  public boolean compareALessThanB(final int a, final int b) {
    // Compare a and b using <
    return a < b;
  }

  public boolean compareALessThanOrEqualToB(final int a, final int b) {
    // Compare a and b using <=
    return a <= b;
  }
}
